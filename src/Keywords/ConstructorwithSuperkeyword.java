package Keywords;

public class ConstructorwithSuperkeyword {

	public static void main(String[] args) {
		

	}

	public ConstructorwithSuperkeyword(int a) {                          //Single parameter constructor
		
		System.out.println("This is Single parameter constructor"+a);
	}
	
	

	public ConstructorwithSuperkeyword(int a, int b) {                     //Two parameter constructor
		
		System.out.println("This is Two parameter constructor"+ a);
		System.out.println("This is Two parameter constructor"+ b);
		
	}

}
