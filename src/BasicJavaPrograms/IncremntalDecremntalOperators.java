package BasicJavaPrograms;

public class IncremntalDecremntalOperators {

	public static void main(String[] args) {
	
		int i=1;   // Initialization
		
		int j =i++;   // post increment operator
		
		

		//Post increment: Here First print the value of variable and then it will increase by 1 
		
		

		
		System.out.println("The value of i is :"+i);   // 2
		
		System.out.println("The value of j is :"+j);   // 1
		
		
		System.out.println("=====================================================================================================");
		
		
		
		int m =1;
		
		int n=m--;   // post increment operator
		
		
		//Post decrement: Here First print the value of variable and then it will decreased by 1 
		

		
      System.out.println("The value of m is :"+m);   // 0
		
		System.out.println("The value of n is :"+n);   // 1
		
		
		System.out.println("=====================================================================================================");	
		
		
		
		int a=1;
		int b=++a;  // pre incremental 
		
		

		//Pre increment: Here first value of variable is increased by 1 and then it will print 
		

		
	      System.out.println("The value of a is :"+a);   // 2
			
			System.out.println("The value of b is :"+b);   // 2
		
		
		
			System.out.println("=====================================================================================================");	
			
		int x=1;
		
		int y =--x;
		
		
		

		//Pre decrement: Here first value of variable is decreased by 1 and then it will print 
		

		
		  System.out.println("The value of x is :"+x);   // 0
			
			System.out.println("The value of y is :"+y);   // 0
		

	}

}
