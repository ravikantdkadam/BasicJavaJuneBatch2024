package CommonJavaPrograms;

public class StarBox {

	
	public static void main(String[] args) {
		
		
		for (int i=1; i<=3;i++) {   // outer loop for number of rows 
			
			
			
			for(int j=1;j<=5;j++) {   //   inner loop for number of columns  
				
				System.out.print("*");
				
			}
			
	          System.out.println("");
	
		}
		
	}

}
