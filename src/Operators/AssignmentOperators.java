package Operators;

public class AssignmentOperators {

	public static void main(String[] args) {
		
		// This operators used to assigne the values for the variable 
		
		
		int x=3;
		
		x+=5;                     //8 (3+5)
		
		x-=2;               //6
		
		x*=3;           //18
		
		x/=2;                //9
		
		x%=9;          //0
		
		
		
		
		System.out.println(x);     
	}

}
