package Operators;

public class RelationalOperators {

	public static void main(String[] args) {
		
		/*Comparison(Relational) Operators :
				==  Equal to    :      x==y            //
				! = Not Equal to :  x!=y
				> Greater than  : x>y
				< Less than :     x<y
				>= Greater than or equal to :x>=y
				< = Less than or equal to : x<=y   */
		
		
		int x=10 ;
		
		int y=3;
		
		
		System.out.println((x==y));   // return the false value 
		System.out.println((x!=y));   // return the true value
		
		System.out.println(x>y);     // return the true value 
		
		System.out.println(x<y);     // return the false value 
		
		System.out.println(x>=y);     // return the true value 
		
		System.out.println(x<=y);     // return the false value 
		
		
	}

}
